/**
 * Interface Lik skal sjekke om et objekt er 
 * identiskt med et annet objekt det sjekkes opp
 * imot. 
 * @param navn, navnet knyttet til objektet
 * @return <CODE>true</CODE> hvis objektet er identiskt
 * @return <CODE>false</CODE> hvis uidentiskt.
 */

interface Lik {
    public Boolean samme(Object l);
}
